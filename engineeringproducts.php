    <div class="type_bearing"> <b>4.  Spherical Roller Bearings</b>
      <p>Spherical Roller Bearings are rolling element bearings that help rotation with low friction and it also allows angular misalignment too. The misalignment is permitted due to the spherical internal shape of the outer ring and spherical rollers. Spherical roller bearings support rotating shaft into the bore of an inner ring that could be misaligned as compared to the outer ring.</p>
      <p>As their name represents, they are NOT truly spherical in shape, the rolling elements in roller bearings are more cylindrical in shape, slightly over-inflated. Spherical roller bearings are used in varieties of industrial applications that have heavy loads and moderate speeds. Most common applications are wind turbines, gearboxes, pumps, mechanical fans, construction equipment, marine propulsions, off-roading vehicles, etc.</p>
