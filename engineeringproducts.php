    <div class="type_bearing"> <b>1. Ball Bearings</b>
      <p>Ball Bearings are probably one of the most common types of bearings that are useful for different purposes. These types of bearings are found in applications where there is lesser load, hence this type of bearing can handle radial and thrust load having the wide range of operating speeds.</p>
      <p>Ball bearings are pretty much versatile in nature. Different applications where ball bearings are used are gear reducers, electric motors, and pumps. Apart from this, ball bearings also serve different purposes for heavy industrial equipment like pumps, extruders, large motors, mining, oil and gas drilling and mining applications</p>
