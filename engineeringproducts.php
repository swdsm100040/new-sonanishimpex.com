    <div class="type_bearing"> <b>2. Cylindrical Roller Bearings</b>
      <p>Cylindrical Roller Bearings are made to hold heavy radial loads, they are usually used in applications like conveyor belt rollers, etc. In this case, the bearing has a roller that is cylinder hence the point of contact is a line. This helps to spread the load over the larger area that allows the bearing to handle greater loads in comparison to a ball bearing. Cylindrical bearings are made with the single row, two rows or multi-rows of rolling elements that help to meet an application’s radial load requirement.</p>
      <p>Roller bearings are available in many standard series and configurations that are used in the wide number of industries. The unique characteristic of having a high load capacity, industrial interchange ability and moderate speed rating makes its design, the most popular bearing designs available.</p>
